    static String evaluate(String exp, boolean err) {
        if (exp.isEmpty()) {
            return "";
        }
        
        if (err && errorCheck(exp)) {// only want to check for errors once, not in each recursive call
            return "ERROR";
        }
        
        ArrayList<String> operands = new ArrayList<>();
        ArrayList<Character> operators = new ArrayList<>();
        
        int start = 0;
        for (int i = 0; i < exp.length(); i++) {
            if (i == exp.length() - 1) {
                operands.add(exp.substring(start));
            } else if (exp.charAt(i) == '(') {
                int bracketCount = 1;
                for (int j = i + 1; j < exp.length(); j++) {
                    if (exp.charAt(j) == '(') {
                        bracketCount++;
                    } else if (exp.charAt(j) == ')') {
                        bracketCount--;
                    }
                    if (bracketCount == 0) {
                        if (j == exp.length() - 1) {
                            operands.add(exp.substring(i));
                            i = exp.length() - 1;
                        } else {
                            if (exp.charAt(j + 1) == '!') {
                                if (j == exp.length() - 2) {
                                    operands.add(exp.substring(i));
                                    i = exp.length() - 1;
                                } else {
                                    operands.add(exp.substring(i, j + 2));
                                    operators.add(exp.charAt(j + 2));
                                    i = j + 2;
                                }
                            } else {
                                operands.add(exp.substring(i, j + 1));
                                operators.add(exp.charAt(j + 1));
                                i = j + 1;
                            }
                            start = i + 1;
                        }
                        break;
                    }
                }
            } else if ("+*-/^".contains(Character.toString(exp.charAt(i)))) {
                operands.add(exp.substring(start, i));
                operators.add(exp.charAt(i));
                start = i + 1;
            }
        }
        
        double d = 0;

        for (int i = 0; i < operands.size(); i++) {
            String op = operands.get(i); // to make more readable
            int len = op.length(); // to make more readable
            if (op.charAt(0) == '(') {
                if (op.charAt(len - 1) == '!') {
                    d = Double.parseDouble(evaluate(op.substring(1, len - 2), false));
                    if (isInteger(d) && d >= 0) {
                        operands.set(i, Integer.toString(fact((int) d)));
                    } else {
                        return "ERROR";
                    }
                } else {
                    operands.set(i, evaluate(op.substring(1, len - 1), false));
                }
            } else if (op.charAt(len - 1) == '!') {
                if (isInteger(Double.parseDouble(op.substring(0, len - 1)))) {
                    operands.set(i, Integer.toString(fact(Integer.parseInt(op.substring(0, len - 1)))));
                } else {
                    return "ERROR";
                }
            }
        }
        
        for (int i = 0; i < operators.size(); i++) {
            if (operators.get(i) == '^') {
                d = Math.pow(Double.parseDouble(operands.get(i)), Double.parseDouble(operands.get(i + 1)));
                operands.set(i, Double.toString(d));
                operands.remove(i + 1);
                operators.remove(i);
                i--;
            }
        }
        
        for (int i = 0; i < operators.size(); i++) {
            if (operators.get(i) == '*' || operators.get(i) == '/') {
                if (operators.get(i) == '*') {
                    d = Double.parseDouble(operands.get(i)) * Double.parseDouble(operands.get(i + 1));
                } else if (operators.get(i) == '/') {
                    if (!operands.get(i + 1).equals("0")) {
                        d = Double.parseDouble(operands.get(i)) / Double.parseDouble(operands.get(i + 1));
                    } else {
                        return "ERROR";
                    }
                }
                operands.set(i, Double.toString(d));
                operands.remove(i + 1);
                operators.remove(i);
                i--;
            }
        }

        for (int i = 0; i < operators.size(); i++) {
            if (operators.get(i) == '+' || operators.get(i) == '-') {
                if (operators.get(i) == '+') {
                    d = Double.parseDouble(operands.get(i)) + Double.parseDouble(operands.get(i + 1));
                } else if (operators.get(i) == '-') {
                    d = Double.parseDouble(operands.get(i)) - Double.parseDouble(operands.get(i + 1));
                }
                operands.set(i, Double.toString(d));
                operands.remove(i + 1);
                operators.remove(i);
                i--;
            }
        }

        return operands.get(0);
    }
    
    static int fact(int num) {
        if (num == 0) {
            return 1;
        }
        return num * fact(num - 1);
    }
    
    static boolean isInteger(double d) {
        return d == Math.floor(d);
    }
    
    static boolean errorCheck(String exp) {
        if ("+*-/^.!)".contains(Character.toString(exp.charAt(0)))
                || "+*-/^.(".contains(Character.toString(exp.charAt(exp.length() - 1)))
                || exp.contains("ERROR")
                || exp.contains("!!")
                || exp.contains("!.")
                || exp.contains("()")) {
            return true;
        }
        
        for (int i = 0; i < exp.length() - 1; i++) {
            if ("+*-/^.".contains(Character.toString(exp.charAt(i)))
                    && "+*-/^.!".contains(Character.toString(exp.charAt(i + 1)))) {
                return true;
            }
        }

        int leftCount = 0;
        int rightCount = 0;
        for (int i = 0; i < exp.length(); i++) {
            if (exp.charAt(i) == '(') {
                leftCount++;
            } else if (exp.charAt(i) == ')') {
                rightCount++;
            }
        }
        return leftCount != rightCount;
    }
    
